package lab9Assignments.Exercise4;

	public class Person {
	    private String name;
	    private int age;

//	    Constructors for name and age of a person
	    public Person(String name, int age) {
	        this.name = name;
	        this.age = age;
	    }

//	    Getters Method to print a Person's age and name
	    public String getName() { 
	    	return name; 
	    	}
	    public Integer getAge() { 
	    	return age; 
	    	}


	}

